var Comment = React.createClass({
  rawMarkup: function() {
    var rawMarkup = marked(this.props.children.toString(), {sanitize: true});
    return { __html: rawMarkup };
  },

  render: function() {
    return (
      <div className="comment">
        <h2 className="commentAuthor">
          {this.props.author}
        </h2>
        <span dangerouslySetInnerHTML={this.rawMarkup()} />
      </div>
    );
  }
});

var CommentBox = React.createClass({
  loadCommentsFromServer: function() {
    $.ajax({
      url: this.props.url,
      dataType: 'json',
      cache: false,
      success: function(data) {
        this.setState({data: data});
      }.bind(this),
      error: function(xhr, status, err) {
        console.error(this.props.url, status, err.toString());
      }.bind(this)
    });
  },
  handleCommentSubmit: function(comment) {
    var comments = this.state.data;
    // Optimistically set an id on the new comment. It will be replaced by an
    // id generated by the server. In a production application you would likely
    // not use Date.now() for this and would have a more robust system in place.
    comment.id = Date.now();
    var newComments = comments.concat([comment]);
    this.setState({data: newComments});
    $.ajax({
      url: this.props.url,
      dataType: 'json',
      type: 'POST',
      data: comment,
      success: function(data) {
        this.setState({data: data});
      }.bind(this),
      error: function(xhr, status, err) {
        this.setState({data: comments});
        console.error(this.props.url, status, err.toString());
      }.bind(this)
    });
  },
  getInitialState: function() {
    return {data: []};
  },
  componentDidMount: function() {
    this.loadCommentsFromServer();
    setInterval(this.loadCommentsFromServer, this.props.pollInterval);
  },
  render: function() {
    return (
      <div className="commentBox">
        <h1>Comments</h1>
        <CommentList data={this.state.data} />
        <CommentForm onCommentSubmit={this.handleCommentSubmit} />
      </div>
    );
  }
});

var CommentList = React.createClass({
  initialized: false,

  render: function() {
    Polymer.Base.async(function() {
      if (!this.initialized) {
        this.initializeGrid();
        this.initialized = true;
      };
    }.bind(this));
    return (
      <vaadin-grid ref="grid" selection-mode="disabled">
        <table>
          <colgroup>
            <col name="id" />
            <col name="author" />
            <col name="text" />
          </colgroup>
          <thead>
            <tr>
              <th>Timestamp</th>
              <th>Author</th>
              <th>Comment</th>
            </tr>
          </thead>
        </table>
      </vaadin-grid>
    );
  },

  initializeGrid: function() {
    this.refs.grid.columns[0].renderer = function(cell) {
      // We happen to know that the id is always a timestamp.
      var timestamp = new Date(cell.data);
      cell.element.textContent = (timestamp.getMonth() + 1) + '/' + timestamp.getDate() + '/' + timestamp.getFullYear();
    };
  },

  componentWillReceiveProps: function(newProps) {
    // Compare length to avoid re-rendering grid constantly.
    if (this.props.data.length !== newProps.data.length) {
      this.refs.grid.items = newProps.data;
    }
  }
});

var CommentForm = React.createClass({
  initialized: false,
  getInitialState: function() {
    return {author: '', text: ''};
  },
  handleAuthorChange: function(e) {
    this.setState({author: e.target.value});
  },
  handleTextChange: function(e) {
    this.setState({text: e.target.value});
  },
  handleSubmit: function(e) {
    e.preventDefault();
    var author = this.state.author.trim();
    var text = this.state.text.trim();
    if (!text || !author) {
      return;
    }
    this.props.onCommentSubmit({author: author, text: text});
    this.setState({author: '', text: ''});
  },
  render: function() {
    if (!this.initialized) {
      Polymer.Base.async(function() {
        this.refs.comment.items = [
          'Ok, sounds good.',
          'Nope, not going to happen.'
        ];
        this.refs.comment.addEventListener('value-changed', function(e) {
          this.handleTextChange(e);
        }.bind(this));
      }.bind(this));
      this.initialized = true;
    }

    return (
      <form className="commentForm" onSubmit={this.handleSubmit}>
        <input
          type="text"
          placeholder="Your name"
          value={this.state.author}
          onChange={this.handleAuthorChange}
        />
        <vaadin-combo-box
          ref="comment"
          label="Say something..."
          allow-custom-value
          value={this.state.text}>
        </vaadin-combo-box>
        <input type="submit" value="Post" />
      </form>
    );
  }
});

window.addEventListener('WebComponentsReady', function() {
  ReactDOM.render(
    <CommentBox url="/api/comments" pollInterval={2000} />,
    document.getElementById('content')
  );
});
